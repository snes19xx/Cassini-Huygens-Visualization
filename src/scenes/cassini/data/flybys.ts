// src/scenes/cassini/data/flybys.ts
//
// Flyby events for the 38-event timeline. Each entry has a unique `id` and
// a body name (titan/iapetus/enceladus/mimas/rhea/dione). The `posPeak`
// coordinates are widely distributed around the origin — no axis magnitude
// less than 80 to prevent clipping with the spacecraft, and no triple is
// reused. Multiple entries per body (titan_huygens, titan_lakes, etc.) are
// keyed by `id`, not body, so simultaneous flybys of the same moon render
// independently.

